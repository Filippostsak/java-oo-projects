package singleton;

/**
 * The {@code MissionBehavior} interface defines the behavior of embarking on a mission.
 * Classes implementing this interface are expected to provide an implementation
 * for the {@code embarkOnMission} method.
 */
public interface MissionBehavior {

    /**
     * Performs the action of embarking on a mission.
     */
    void embarkOnMission();
}
